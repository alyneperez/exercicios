# Exercício Python #005 - Antecessor e Sucessor

#  Faça um programa que leia um número inteiro e mostre na tela seu sucessor e seu antecessor.

n = int(input("Digite um número inteiro: "))
print(f"O número digitado foi {n}, seu antecessor é {n - 1} e seu sucessor é {n + 1}.")

